    xl_row += 1

    # ── 単位注記
    ws.merge_cells(start_row=xl_row, start_column=1, end_row=xl_row, end_column=6)
    _write(xl_row, 1, "※単位：円（指標は各単位）", bg=C_WHITE, fg="FF888888", sz=8, ha="left")
    ws.row_dimensions[xl_row].height = 13
    xl_row += 1

    # ── ヘッダー行
    for c, (txt, ha) in enumerate([
        ("分類","center"),("指標名","left"),("単位","center"),
        (p_labels["前々期"],"center"),(p_labels["前期"],"center"),(p_labels["今期"],"center")
    ], 1):
        _write(xl_row, c, txt, bg=C_HDR, fg=C_FG_W, bold=True, ha=ha)
