def _sum_v(row_min, row_max, col):
    s = 0.0
    for r in range(row_min, row_max + 1):
        # 89行目（当期労務費 合計）はSUMIF対象から除外
        if r == 89:
            continue
        row = data_dict.get(r, {})
        kubun = str(row.get("区分", "") or "").strip().upper()
        if kubun in ["V", "Ｖ"]:
            s += float(row.get(col, 0) or 0)
    return s

