    xl_row += 1

    # ── データ行ヘルパー
    def write_data_row(r, label, unit, values, class_label=None, class_rows=None, class_bg=C_CLASS1):
        """values = [(val, fmt), ...]  前々期・前期・今期 順"""
        # 分類列
        if class_label is not None:
            ws.merge_cells(start_row=r, start_column=1,
                           end_row=r + class_rows - 1, end_column=1)
            c = ws.cell(r, 1)
            c.value     = class_label
            c.fill      = _fill(class_bg)
            c.font      = _font(C_FG_D, bold=True, sz=9)
            c.alignment = Alignment(horizontal="center", vertical="center",
                                    wrap_text=True, text_rotation=255)
            c.border    = _bdr()
        # 指標名
        _write(r, 2, label, bg=C_LABEL, fg=C_FG_N, ha="left")
        # 単位
        _write(r, 3, unit,  bg=C_LABEL, fg=C_FG_N, ha="center")
        # 数値
        for ci, (val, fmt) in enumerate(values, 4):
            if val is None:
                _write(r, ci, "―", bg=C_WHITE, fg=C_FG_N, ha="right")
            else:
                _write(r, ci, val, bg=C_WHITE, fg=C_FG_N, ha="right", fmt=fmt)
        ws.row_dimensions[r].height = 15

