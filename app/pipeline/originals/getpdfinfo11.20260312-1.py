def generate_keiei_shihyo_html(data_dict, closing_dates):
    """
    data_dict から経営指標を生成する。
    行番号マッピング（data_dict行番号）:
      資本金=66, 従業員数=159, 売上高=112, 売上総利益=120,
      営業利益=140, 経常利益=149, 税引後利益=154, 減価償却費=161,
      現預金残高=1, 純資産=74, 総資産=75, 借入金残高=163
    """
    pkeys = ["前々期", "前期", "今期"]

    def rv(row_no, pk):
        r = data_dict.get(row_no, {})
        v = r.get(pk, 0)
        try:
            return float(v) if v not in (None, "", '""') else 0.0
        except (ValueError, TypeError):
            return 0.0

    def fmt_num(v, unit='千円'):
        """数値を単位に応じてフォーマット（3桁カンマ区切り）"""
        if v is None: return '―'
        try:
            f = float(v)
            if unit in ('円', '人', '千円', ''):
                return f'{int(round(f)):,}'
            elif unit in ('％', '年', '回'):
                return f'{f:.2f}'
            else:
                return f'{int(round(f)):,}'
        except:
            return '―'

    def safe_pct(a, b):
        """a/b*100、ゼロ除算はNone"""
        return (a / b * 100) if b else None

    def safe_div(a, b):
        """a/b、ゼロ除算はNone"""
        return (a / b) if b else None

    # 期間ラベル
    p_labels = {pk: (closing_dates.get(pk) or pk) for pk in pkeys}

    # --- 基本指標の取得 ---
    def base(pk):
        cap    = rv(66,  pk)  # 資本金
        emp    = rv(159, pk)  # 従業員数
        sales  = rv(112, pk)  # 売上高
        gross  = rv(120, pk)  # 売上総利益
        op     = rv(140, pk)  # 営業利益
        ord_p  = rv(149, pk)  # 経常利益
        net    = rv(154, pk)  # 税引後利益
        dep    = rv(161, pk)  # 減価償却費
        cash   = rv(1,   pk)  # 現預金残高
        equity = rv(74,  pk)  # 純資産合計
        assets = rv(75,  pk)  # 総資産（純資産・負債合計）
        borrow = rv(163, pk)  # 借入金残高
        return dict(cap=cap, emp=emp, sales=sales, gross=gross,
                    op=op, ord_p=ord_p, net=net, dep=dep,
                    cash=cash, equity=equity, assets=assets, borrow=borrow)

    bases = {pk: base(pk) for pk in pkeys}

    # --- 分析指標の計算 ---
    def calc_indices(b):
        sales, gross, op, ord_p, net, dep, equity, assets, borrow = (
            b['sales'], b['gross'], b['op'], b['ord_p'], b['net'],
            b['dep'], b['equity'], b['assets'], b['borrow'])
        return dict(
            gross_margin  = safe_pct(gross, sales),       # 売上高粗利益率
            op_margin     = safe_pct(op,    sales),       # 売上高営業利益率
            ord_margin    = safe_pct(ord_p, sales),       # 売上高経常利益率
            equity_ratio  = safe_pct(equity, assets),     # 自己資本比率
            debt_payback  = safe_div(borrow, net + dep),  # 債務償還年数
            asset_turnover= safe_div(sales, assets),      # 総資本回転率
            roa           = safe_pct(ord_p, assets),      # 総資本経常利益率
        )

    indices = {pk: calc_indices(bases[pk]) for pk in pkeys}

    # --- HTML構築 ---
    H = '#1B4F72'; W = '#ffffff'; LB = '#AED6F1'; G = '#f2f2f2'

    def th(txt, bg=H, fg=W, align='center', colspan=1, rowspan=1):
        cs = f' colspan="{colspan}"' if colspan > 1 else ''
        rs = f' rowspan="{rowspan}"' if rowspan > 1 else ''
        return (f'<th{cs}{rs} style="background:{bg};color:{fg};padding:5px 8px;'
                f'border:1px solid #aaa;font-size:12px;text-align:{align};'
                f'white-space:nowrap;">{txt}</th>')

    def td(txt, bg=W, fg='#333', align='right', bold=False, keiei_key=''):
        bw = 'font-weight:bold;' if bold else ''
        kattr = f' data-keiei-key="{keiei_key}"' if keiei_key else ''
        return (f'<td{kattr} style="background:{bg};color:{fg};{bw}padding:4px 8px;'
                f'border:1px solid #ddd;font-size:12px;text-align:{align};'
                f'white-space:nowrap;">{txt}</td>')

    def td_label(txt, bg=G, indent=0):
        pad = f'padding:4px 8px 4px {8+indent*12}px;'
        return (f'<td style="background:{bg};color:#333;{pad}'
                f'border:1px solid #ddd;font-size:12px;white-space:nowrap;">{txt}</td>')

    def td_classify(title, rowspan):
        """分類列：縦結合セル（Excelの A9:A20 等に相当）"""
        return (f'<td rowspan="{rowspan}" style="background:#AED6F1;color:#1a3a5c;'
                f'font-weight:bold;padding:5px 8px;border:1px solid #aaa;'
                f'font-size:12px;text-align:center;vertical-align:middle;'
                f'writing-mode:vertical-rl;letter-spacing:0.15em;'
                f'white-space:nowrap;">{title}</td>')

    # ヘッダー行
    header = ('<tr>'
              + th('分類', rowspan=1)
              + th('指標名', align='left')
              + th('単位')
              + ''.join(th(p_labels[pk]) for pk in pkeys)
              + '</tr>')

    rows_html = []

    # === 経営基本指標 ===

    basic_items = [
        ('資本金',     'cap',    '円'),
        ('従業員数',   'emp',    '人'),
        ('売上高',     'sales',  '円'),
        ('売上総利益', 'gross',  '円'),
        ('営業利益',   'op',     '円'),
        ('経常利益',   'ord_p',  '円'),
        ('税引後利益', 'net',    '円'),
        ('減価償却費', 'dep',    '円'),
        ('現預金残高', 'cash',   '円'),
        ('純資産',     'equity', '円'),
        ('総資産',     'assets', '円'),
        ('借入金残高', 'borrow', '円'),
    ]
    for bi, (label, key, unit) in enumerate(basic_items):
        row = '<tr>'
        if bi == 0:
            row += td_classify('経営基本指標の変化', len(basic_items))
        row += td_label(label, indent=1)
        row += td(unit, align='center', bg=G)
        for pk in pkeys:
            v = bases[pk][key]
            row += td(fmt_num(v, unit), keiei_key=f'basic-{key}-{pk}')
        row += '</tr>'
        rows_html.append(row)

    # === 経営指標分析 ===

    index_items = [
        ('売上高粗利益率',   'gross_margin',   '％'),
        ('売上高営業利益率', 'op_margin',       '％'),
        ('売上高経常利益率', 'ord_margin',      '％'),
        ('自己資本比率',     'equity_ratio',    '％'),
        ('債務償還年数',     'debt_payback',    '年'),
        ('総資本回転率',     'asset_turnover',  '回'),
        ('総資本経常利益率', 'roa',             '％'),
    ]
    for ii, (label, key, unit) in enumerate(index_items):
        row = '<tr>'
        if ii == 0:
            row += td_classify('経営指標分析', len(index_items))
        row += td_label(label, indent=1)
        row += td(unit, align='center', bg=G)
        for pk in pkeys:
            v = indices[pk][key]
            row += td(fmt_num(v, unit), keiei_key=f'idx-{key}-{pk}')
        row += '</tr>'
        rows_html.append(row)

    table = (
        '<table style="border-collapse:collapse;width:100%;'
        'font-family:Meiryo,sans-serif;min-width:500px;">'
        '<thead>' + header + '</thead>'
        '<tbody>' + ''.join(rows_html) + '</tbody>'
        '</table>'
    )

    acc_id = 'keiei_shihyo'
    return (
        f'<div class="accordion-section">'
        f'<button class="accordion-toggle collapsed" onclick="accToggle(\'{acc_id}\')">'
        f'経営指標<span class="acc-arrow">▼</span></button>'
        f'<div class="accordion-body collapsed" id="acc-{acc_id}">'
        f'<p style="margin:8px 0 6px;font-size:11px;color:#666;">※単位：円（指標は各単位）</p>'
        f'{table}</div></div>'
    )



