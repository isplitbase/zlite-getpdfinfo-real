def _write_keiei_sheet(wb, data_dict, closing_dates):
    """経営指標シートを wb に追加して ws を返す"""
    from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    from openpyxl.utils import get_column_letter
    from openpyxl.cell.cell import MergedCell as _MC

    ws = wb.create_sheet(title="経営指標")
    ws.sheet_view.showGridLines = False

    _thin = Side(style="thin")
    _none = Side(style=None)
    def _fill(argb): return PatternFill("solid", fgColor=argb)
    def _font(argb, bold=False, sz=9):
        return Font(name="Meiryo", size=sz, color=argb, bold=bold)
    def _align(h="left", v="center", wrap=False):
        return Alignment(horizontal=h, vertical=v, wrap_text=wrap)
    def _bdr(t=True, b=True, l=True, r=True):
        s = lambda x: _thin if x else _none
        return Border(top=s(t), bottom=s(b), left=s(l), right=s(r))
    def _write(r, c, val="", bg="FFFFFFFF", fg="FF333333", bold=False,
               ha="left", fmt=None, sz=9, wrap=False, va="center"):
        cell = ws.cell(r, c)
        if isinstance(cell, _MC): return
        cell.value     = val
        cell.fill      = _fill(bg)
        cell.font      = _font(fg, bold=bold, sz=sz)
        cell.alignment = Alignment(horizontal=ha, vertical=va, wrap_text=wrap)
        cell.border    = _bdr()
        if fmt: cell.number_format = fmt

    # 列幅
    col_widths = [12, 36, 10, 20, 20, 20]
    for ci, w in enumerate(col_widths, 1):
        ws.column_dimensions[get_column_letter(ci)].width = w

    pkeys = ["前々期", "前期", "今期"]
    p_labels = {pk: (closing_dates.get(pk) or pk) for pk in pkeys}

    def rv(row_no, pk):
        r = data_dict.get(row_no, {})
        v = r.get(pk, 0)
        try:
            return float(v) if v not in (None, "", '""') else 0.0
        except: return 0.0

    def safe_pct(a, b): return (a / b * 100) if b else None
    def safe_div(a, b): return (a / b)        if b else None

    def base(pk):
        return dict(
            cap    = rv(66,  pk),
            emp    = rv(159, pk),
            sales  = rv(112, pk),
            gross  = rv(120, pk),
            op     = rv(140, pk),
            ord_p  = rv(149, pk),
            net    = rv(154, pk),
            dep    = rv(161, pk),
            cash   = rv(1,   pk),
            equity = rv(74,  pk),
            assets = rv(75,  pk),
            borrow = rv(163, pk),
        )

    def calc_indices(b):
        s, g, op, ord_p, net, dep, eq, ast, bw = (
            b['sales'], b['gross'], b['op'], b['ord_p'], b['net'],
            b['dep'], b['equity'], b['assets'], b['borrow'])
        return dict(
            gross_margin   = safe_pct(g,     s),
            op_margin      = safe_pct(op,    s),
            ord_margin     = safe_pct(ord_p, s),
            equity_ratio   = safe_pct(eq,    ast),
            debt_payback   = safe_div(bw,    net + dep),
            asset_turnover = safe_div(s,     ast),
            roa            = safe_pct(ord_p, ast),
        )

    bases   = {pk: base(pk)          for pk in pkeys}
    indices = {pk: calc_indices(bases[pk]) for pk in pkeys}

    NUM_FMT  = '#,##0;[Red]-#,##0;0'
    PCT_FMT  = '0.00;[Red]-0.00;0.00'
    YEAR_FMT = '0.00;[Red]-0.00;0.00'

    # 色
    C_TITLE  = "FF1B4F72"
    C_HDR    = "FF2E86C1"
    C_CLASS1 = "FFAED6F1"   # 経営基本指標 分類列
    C_CLASS2 = "FFD5E8D4"   # 経営指標分析 分類列
    C_LABEL  = "FFF2F2F2"
    C_WHITE  = "FFFFFFFF"
    C_FG_W   = "FFFFFFFF"
    C_FG_D   = "FF1a3a5c"
    C_FG_N   = "FF333333"

    xl_row = 1

    # ── タイトル行
    ws.merge_cells(start_row=xl_row, start_column=1, end_row=xl_row, end_column=6)
    _write(xl_row, 1, "経営指標", bg=C_TITLE, fg=C_FG_W, bold=True, sz=11, ha="left")
