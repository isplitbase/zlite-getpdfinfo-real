def _xl_left_layout(row_start, row_end):
    layout = {}
    for d in _SPEC_CELL_DEFS:
        if d["c0"] >= 4 or d["c1"] <= 0: continue
        if d["r1"] <= row_start or d["r0"] > row_end: continue
        r0 = max(d["r0"], row_start); r1 = min(d["r1"], row_end + 1)
        c0 = max(d["c0"], 0);         c1 = min(d["c1"], 4)
        for r in range(r0, r1):
            for c in range(c0, c1):
                existing = layout.get((r, c))
                # ★ c0が大きい（より右寄り）定義を優先する
                # 同じc0なら後から来たものを使う（先に登録された広い定義を狭い定義で上書き）
                if existing is None or d["c0"] >= existing["def"]["c0"]:
                    layout[(r, c)] = {
                        "def": d, "is_topleft": (r == r0 and c == c0),
                        "r0": r0, "r1": r1, "c0": c0, "c1": c1,
                    }
    return layout

